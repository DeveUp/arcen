class TypeFurniture():

    def __init__(self):
        self.id = None
        self.number_type_furniture = None
        self.count_rack = None
        self.count_row = None
        self.depth = None
        self.height = None
        self.width = None
        self.date = None
    
    def get_id(self):
        return self.id

    def set_id(self, id):
        self.id = id

    def get_number_type_furniture(self):
        return self.number_type_furniture

    def set_number_type_furniture(self, number_type_furniture):
        self.number_type_furniture = number_type_furniture

    def get_count_rack(self):
        return self.count_rack

    def set_count_rack(self, count_rack):
        self.count_rack = count_rack

    def get_count_row(self):
        return self.count_row

    def set_count_row(self, count_row):
        self.count_row = count_row
    
    def get_depth(self):
        return self.depth

    def set_depth(self, depth):
        self.depth = depth

    def get_height(self):
        return self.height

    def set_height(self, height):
        self.height = height

    def get_width(self):
        return self.width

    def set_width(self, width):
        self.width = width

    def get_date(self):
        return self.date
    
    def set_date(self, date):
        self.date = date